package javacore.aula03;

public class ExemploArraysMatriz {
    public static void main(String[] args) {

        int[][] matriz = new int[3][3]; // 3 coluna e 3 linha
        matriz[0][0] = 1;
        matriz[0][1] = 2;
        matriz[0][2] = 3;
        matriz[1][0] = 4;



        int[][][] matriz3d = new int[5][5][5];
        matriz3d[0][0][0] = 1;
        matriz3d[0][1][0] = 2;
        matriz3d[0][2][2] = 3;
        matriz3d[0][3][3] = 4;

    }
}
